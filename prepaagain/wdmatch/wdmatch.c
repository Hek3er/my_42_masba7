#include <unistd.h>

int main(int ac, char *av[])
{
	int i;
	char *tmp;

	i = 0;
	if (ac == 3)
	{
		tmp = av[1];
		while (*tmp && *av[2])
		{
			while (*tmp == *av[2] &&  (*tmp && *av[2]))
			{
				tmp++;
				av[2]++;
			}
			av[2]++;
		}
		if (!(*tmp))
		{
			while (av[1][i] != '\0')
			{
				write(1, &av[1][i] ,1);
				i++;
			}
		}
	}
	write(1, "\n", 1);
}
