int	ft_atoi(const char *str)
{
	int i;
	int neg;
	int ret;

	neg = 1;
	i = 0;
	ret = 0;
	while (str[i] != '\0' && str[i] == ' ' && (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
		{
			neg *= -1;
		}
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		ret = ret * 10 +(str[i] - 48);
		i++;
	}
	return (ret * neg);
}
