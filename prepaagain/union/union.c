#include <unistd.h>

int main(int ac, char *av[])
{
	int i;
	int j;
	int T[127] = {0};

	i = 1;
	if (ac == 3)
	{
		while (i < ac)
		{
			j = 0;
			while (av[i][j] != '\0')
			{
				if (T[(int)av[i][j]] == 0)
				{
					write(1, &av[i][j], 1);
					T[(int)av[i][j]] = 1;
				}
				j++;
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
