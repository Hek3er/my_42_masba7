#include <unistd.h>

int main(int ac, char *av[])
{
    int i;
    int T[127] = {0};
    if(ac ==3)
    {
        i = 0;
        while(av[2][i])
        {
            T[(int)av[2][i++]] = 1;
        }
        i = 0;
        while (av[1][i])
        {
            if (T[(int)av[1][i]] == 1)
            { 
                write(1, &av[1][i], 1);
                T[(int)av[1][i]] = 2;
            }
            i++;
        }
    }
    write(1, "\n", 1);
    return 0;
}