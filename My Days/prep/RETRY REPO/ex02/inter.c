#include <unistd.h>

int main(int ac, char *av[])
{
    int i;
    int T[127] = {0};

    i = 0;
    if (ac == 3)
    { 
        while (av[2][i] != '\0')
        {
            T[(int)av[2][i]] = 1;
            i++;
        }
        i = 0;
        while (av[1][i] != '\0')
        {
            if (T[(int)av[1][i]] == 1)
            {
                write(1, &av[1][i], 1);
                T[(int)av[1][i]] = 2;
            }
            i++;
        }
    }
    write(1, "\n", 1);
}